#include<iostream>
using namespace std;
/*特殊字符*/
int main()
{
    // \a : 铃声（报警声）
    cout<<"\a";
    // \b : 回退
    cout<<"\b";
    // \f : 换页

    // \n : 换行

    // \r : 按enter键

    // \t : Tab键

    // \v : 垂直制表

    // \' : 单引号

    // \" : 双引号

    // \? : 问号

    // \\ : 反斜杠

    // \000 : 八进制

    // \xhhh : 十六进制
}