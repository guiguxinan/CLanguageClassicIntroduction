#include <iostream>

using namespace std;

int main()
{
   /*
   关系运算符
   常用于if语句中
   */
  cout<<"符号\t\t\t"<<"功能\t\t\t"<<"例子\t\t\t"<<"比较结果"<<endl;
  cout<<"==\t\t\t"<<"等于\t\t\t"<<"1 == 2\t\t\t"<<int(1 != 2)<<endl; 
  cout<<"!=\t\t\t"<<"不等于\t\t\t"<<"1 != 2\t\t\t"<<int(1 != 2)<<endl;
  cout<<">\t\t\t"<<"大于\t\t\t"<<"1 > 2\t\t\t"<<int(1 > 2)<<endl;
  cout<<"<\t\t\t"<<"小于\t\t\t"<<"1 < 2\t\t\t"<<int(1 < 2)<<endl;
  cout<<">=\t\t\t"<<"大于等于\t\t"<<"1 >= 2\t\t\t"<<int(1 >= 2)<<endl;
  cout<<"<=\t\t\t"<<"小于等于\t\t"<<"1 <= 2\t\t\t"<<int(1 <= 2)<<endl;
   return 0;
}
